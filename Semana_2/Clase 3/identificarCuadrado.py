# pedirle al usuario el alto y el aancho de un rectangulo y decirle si es cuadrado

#paso 1: defino variables
alto = float(input(" Por favor ingrese el alto del rectangulo: "))
ancho = float(input(" por favor ingrese el alto del rectangulo: "))

if alto == ancho:
    print(" Su figura es un cuadrado ")
else:  print(" Su figura es un rectangulo ")



